package plantpulse.cep.domain;

public class AlarmCount {
	
	private int info_count;
	private int warn_count;
	private int error_count;
	
	public int getInfo_count() {
		return info_count;
	}
	public void setInfo_count(int info_count) {
		this.info_count = info_count;
	}
	public int getWarn_count() {
		return warn_count;
	}
	public void setWarn_count(int warn_count) {
		this.warn_count = warn_count;
	}
	public int getError_count() {
		return error_count;
	}
	public void setError_count(int error_count) {
		this.error_count = error_count;
	}
	
	

}
